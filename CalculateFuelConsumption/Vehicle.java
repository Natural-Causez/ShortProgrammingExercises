//Vehicle object class storing horsepower, aerodynamic coefficient and weight with getters

public class Vehicle {
    Double horsepower; Double aerodynamics; Double weight;
    
    public Double gethorsepower() {
        return horsepower;
    }//END gethorsepower
        
    public Double getaerodynamics() {
        return aerodynamics;
    }//END getaerodynamics
        
    public Double getweight() {
        return weight;
    }//END weight
        
}// END class Vehicle
